        "relative flex w-full cursor-pointer select-none items-center rounded-[0.4rem] py-2.5 pl-3 pr-9 text-sm font-medium text-[#f5f5f3] outline-none transition-colors",
        "data-disabled:pointer-events-none data-disabled:opacity-50",
        "data-highlighted:!bg-[#2b2b2b] data-highlighted:!text-[#f5f5f3]",
        "data-[state=checked]:!bg-[#f0ed7e] data-[state=checked]:!text-[#151414]",
        "data-[state=checked]:data-highlighted:!bg-[#f0ed7e] data-[state=checked]:data-highlighted:!text-[#151414]",
